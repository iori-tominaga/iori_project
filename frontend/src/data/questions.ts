// ── ID を付与して構造化 ─────────────────────────────────────────────────
let questionIdCounter = 1;

export const DOMAINS: DataDomain[] = rawDomains.map((d, di) => {
  const domainId = di + 1;
  return {
    id: domainId,
    name: d.name,
    description: d.description,
    questions: d.questions.map(q => {
      const questionId = questionIdCounter++;
      return {
        id: questionId,
        domain_id: domainId,
        domain_name: d.name,
        text: q.text,
        difficulty: q.difficulty,
        choices: q.choices.map((c, ci) => ({
          id: (questionId - 1) * 4 + ci + 1,
          text: c.text,
          is_correct: c.correct,
          order_num: ci + 1,
        })),
        explanation: q.explanation,
      };
    }),
  };
});

export const ALL_QUESTIONS: DataQuestion[] = DOMAINS.flatMap(d => d.questions);
